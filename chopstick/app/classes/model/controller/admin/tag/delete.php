<?php
namespace app\model\controller\admin\tag;

use \core\db;

class delete extends \app\model\controller\admin\tag\base
{
    // ################################################################################
    // 検証
    // ################################################################################
    // ------------------------------------------------------------
    // 検証（削除）
    // ------------------------------------------------------------
    public function check()
    {
        return $this->is_valid;
    }
    // ################################################################################
    // データ操作
    // ################################################################################
    // --------------------------------------------------------------------------------
    // データ操作（削除）
    // --------------------------------------------------------------------------------
    public function delete()
    {
        $con = new db();
        //
        $sql = <<< EOT
DELETE
FROM
    cs_tag
WHERE
    tag_id = :tag_id;
EOT;
        $sql_params = array
        (
            ':tag_id' => $this->get_value('tag_id'),
        );
        //
        $con->query($sql, $sql_params);
    }
}