<?php
namespace app\model\controller\admin\stack;

use \core\db;

class delete extends \app\model\controller\admin\stack\base
{
    //
    // ################################################################################
    //
    // 検証
    //
    // ################################################################################
    //
    // --------------------------------------------------------------------------------
    // 検証（削除）
    // --------------------------------------------------------------------------------
    //
    public function check()
    {
        return $this->is_valid;
    }
    //
    // ################################################################################
    //
    // データ操作
    //
    // ################################################################################
    //
    // --------------------------------------------------------------------------------
    // 削除
    // --------------------------------------------------------------------------------
    //
    public function delete()
    {
        $con = new db();
        //
        $sql = <<< EOT
DELETE
FROM
    cs_stack
WHERE
stack_key = :stack_key;
EOT;
        $sql_params = array
        (
            ':stack_key' => $this->get_value('stack_key'),
        );
        //
        $con->query($sql, $sql_params);
    }
}