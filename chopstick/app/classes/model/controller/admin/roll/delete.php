<?php
namespace app\model\controller\admin\roll;

use \core\db;

class delete extends \app\model\controller\admin\roll\base
{
    // ################################################################################
    // 検証
    // ################################################################################
    // --------------------------------------------------------------------------------
    // 検証（削除）
    // --------------------------------------------------------------------------------
    public function check()
    {
        return $this->is_valid;
    }
    // ################################################################################
    // データ操作
    // ################################################################################
    // --------------------------------------------------------------------------------
    // 削除
    // --------------------------------------------------------------------------------
    public function delete()
    {
        $con = new db();
        //
        $sql = <<< EOT
DELETE
FROM
    cs_roll
WHERE
    roll_key = :roll_key AND
    reserved = 0;
EOT;
        $sql_params = array
        (
            ':roll_key' => $this->get_value('roll_key'),
        );
        $con->query($sql, $sql_params);
    }
}