<?php
namespace app\model\controller\admin\user;

use \core\db;

class delete extends \app\model\controller\admin\user\base
{
    //
    // ********************************************************************************
    // ****
    // **** 検証
    // ****
    // ********************************************************************************
    //
    // ------------------------------------------------------------
    // 検証（削除）
    // ------------------------------------------------------------
    //
    public function check()
    {
        return $this->is_valid;
    }
    //
    // ********************************************************************************
    //
    // データ操作
    //
    // ********************************************************************************
    //
    // --------------------------------------------------------------------------------
    // データ操作（削除）
    // --------------------------------------------------------------------------------
    //
    public function delete()
    {
        $con = new db();
        //
        $sql = <<< EOT
DELETE
FROM
    cs_user
WHERE
    user_id = :user_id;
EOT;
        $sql_params = array
        (
            ':user_id' => $this->get_value('user_id'),
        );
        //
        $con->query($sql, $sql_params);
    }
}