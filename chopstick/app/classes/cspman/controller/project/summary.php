<?php
namespace app\cspman\controller\project;

use \core\db;
use \core\response;
use \core\url;

use \app\cspman\model\controller\project\summary as rs_project;

class summary extends \app\controller_admin
{
    // ********************************************************************************
    // **** アクション
    // ********************************************************************************
    // --------------------------------------------------------------------------------
    // コントローラー起動事前処理
    // --------------------------------------------------------------------------------
    public function before()
    {
        parent::before();
    }
    // --------------------------------------------------------------------------------
    // 既定
    // --------------------------------------------------------------------------------
    public function action_index($params)
    {
        $this->display();
    }
    // --------------------------------------------------------------------------------
    // 並べ替えアクション
    // --------------------------------------------------------------------------------
    public function action_sort($params)
    {
        $rs_project = new rs_project();
        $rs_project->sort($params);
    }
    // ********************************************************************************
    // **** 表示
    // ********************************************************************************
    // --------------------------------------------------------------------------------
    // 表示
    // --------------------------------------------------------------------------------
    private function display()
    {
        $rs_project = new rs_project();
        //
        $vars = array
        (
            'rs_project' => $rs_project->fetch_all(),
        );
        echo $this->render('cspman/controller/project/summary.twig', $vars);
    }
}