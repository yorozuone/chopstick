<?php
namespace app\csroot\controller\admin\roll;

use \core\db;
use \core\response;
use \core\url;

// cs_roll
use \app\csroot\model\controller\admin\roll\summary as rs_roll;

class summary extends \app\controller_admin
{
    private $rs_roll;
    // ********************************************************************************
    // **** アクション
    // ********************************************************************************
    // --------------------------------------------------------------------------------
    // コントローラー起動事前処理
    // --------------------------------------------------------------------------------
    public function before()
    {
        parent::before();
        $this->rs_roll = new rs_roll;
    }
    // --------------------------------------------------------------------------------
    // 既定
    // --------------------------------------------------------------------------------
    public function action_index()
    {
        $this->display();
    }
    // --------------------------------------------------------------------------------
    // 並べ替えアクション
    // --------------------------------------------------------------------------------
    public function action_sort($params)
    {
        $this->rs_roll->sort($params);
    }
    // ********************************************************************************
    // **** 表示
    // ********************************************************************************
    // --------------------------------------------------------------------------------
    // 表示
    // --------------------------------------------------------------------------------
    private function display()
    {
        $vars = array
        (
            'rs_roll' => $this->rs_roll->fetch_all(),
        );
        echo $this->render('cscms/controller/admin/roll/summary.twig', $vars);
    }
}
